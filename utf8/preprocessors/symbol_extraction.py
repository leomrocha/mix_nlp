def extract_chars(fname, save_output=False):
    with open(fname, 'r') as f:
        chars = {'\n', '\t', ' '}
        txt = f.read()
        for c in txt:
            chars.add(c)
        lines = sorted(list(chars))
        # print("Extracted {} chars from file: {}".format(len(lines), fname))
        if save_output:
            with open(fname.replace('.txt', '.chars'), 'w') as of:
                of.writelines(lines)
                of.flush()
    return chars


def extract_all_chars(path=PATH):
    files = [os.path.join(path, f) for f in os.listdir(path) if f.endswith('.txt')]
    for f in files:
        extract_chars(f, save_output=True)


def char_stats(path=PATH):
    files = [os.path.join(path, f) for f in os.listdir(path) if f.endswith('.txt')]
    all_chars = set([])
    for f in files:
        chars = extract_chars(f, save_output=False)
        print("{} new chars on file {} ".format(len(chars.difference(all_chars)), f))
        all_chars.update(chars)
    lines = sorted(list(all_chars))
    selected_chars = sorted(list(all_chars.difference(set(EXCLUDE + MAYBE_EXCLUDE))))
    print("Total number of symbols: {}".format(len(lines)))
    print("Number of Selected Symbols: {}".format(len(selected_chars)))
    with open(os.path.join(path, "all_chars.chars"), 'w') as f:
        f.writelines(lines)
    with open(os.path.join(path, "all_chars_byline.chars"), 'w') as f:
        lines = [l + '\n' for l in lines]
        f.writelines(lines)
    with open(os.path.join(path, "selected_chars.chars"), 'w') as f:
        f.writelines(selected_chars)
    with open(os.path.join(path, "selected_chars_byline.chars"), 'w') as f:
        lines = [l + '\n' for l in selected_chars]
        f.writelines(lines)
    return selected_chars
